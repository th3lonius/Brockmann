				<h3>Reputations: Josef Müller-Brockmann</h3>
                <author>by Yvonne Schwemer-Scheddin</author>
                <a href="http://www.eyemagazine.com/feature/article/reputations-josef-muller-brockmann">from Eye Magazine</a>
                <time>Winter 1995</time>
				<p>MB: In training young people to be designers, the first thing that must be taught are the laws of design which enable them to develop their own talents and to approach rational projects in a rational way. A predominately artistic foundation may be appropriate for artists, painters and sculptors, but as a teacher of graphic design I have repeatedly impressed upon my students that while it is important to be interested in music, old and new, in theatre and opera, old and new architecture, they must also concern themselves with town planning, environmental issues and politics.</p>
